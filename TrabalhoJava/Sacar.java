    private void sacarefActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sacarefActionPerformed
        // TODO add your handling code here:
            
        double valorsaque = Integer.parseInt(valSaque.getText());  //valor a ser retirado
        int numerocont = Integer.parseInt(numConta.getText());  // numero da conta
        
        int ver = 0;
        
        if(numerocont == Principal.cc.getNumeroCorrente()){
            ver = 1;
            
            Principal.cc.setSacar(valorsaque);
            
            if(Principal.cc.flag == 1)
            {   
                
                JOptionPane.showMessageDialog(null,"Conta"+Principal.cc.getTipoConta()+"\n"+"Saque efetuado com sucesso! \nSaldo atual: "+ Principal.cc.getSaldoCorrente()); 
   
            }
                
        }
        
        if(numerocont == Principal.cp.getNumeroPoupanca())
        {
            ver = 1;
            
            Principal.cp.setSacar(valorsaque);
            
            if(Principal.cp.flag == 1)
            {
                
                JOptionPane.showMessageDialog(null,"Conta"+Principal.cp.getTipoConta()+"\n"+"Saque efetuado com sucesso! \nSaldo atual: "+ Principal.cp.getSaldoPoupanca()); 
        
            }
            
        }
        
        if(numerocont == Principal.cep.getNumeroEspecial())
        {
            ver = 1;
            
            Principal.cep.setSacar(valorsaque);
            if(ContaEspecial.flag == 1){
                JOptionPane.showMessageDialog(null,"Conta"+Principal.cep.getTipoConta()+"\n"+"Saque efetuado com sucesso! \nSaldo atual: "+ Principal.cep.getSaldoEspecial()); 
            }
            
        }
        
        if(ver == 0){
            JOptionPane.showMessageDialog(null, "Erro! Verifique se seus dados est√£o corretos e tente novamente.");
        }
        
    }//GEN-LAST:event_sacarefActionPerformed

